#include "libft.h"

double	ft_pow(double x, int n)
{
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n = -n;
		x = 1 / x;
	}
	if (n % 2 == 0)
		return (ft_pow(x * x, n / 2));
	return (x * ft_pow(x, n - 1));
}
